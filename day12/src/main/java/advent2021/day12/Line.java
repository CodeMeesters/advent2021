package advent2021.day12;

public class Line {
  
  private String mNodeA;
  private String mNodeB;

  public Line(String pNodeA, String pNodeB) {
    mNodeA = pNodeA;
    mNodeB = pNodeB;
  }
  
  public String getNodeA() {
    return mNodeA;
  }
  
  public String getNodeB() {
    return mNodeB;
  }
  
}
